	<h2>What is this?</h2>
	<p>IMDb needs a better and more modern design. This is a minimal concept I made to allow me to access the awesome content IMDb has without having to suffer through their interface.
	<p>This is not a fully functioning redesign. It's a simple concept that only covers the basics of browsing movie and actor information. Things like rating movies, the watchlist, the message boards, and editing content are not and will probably not be supported due to technical limitations.
	<p>The design was also highly influenced by the structure of the current IMDb site to minimize development efforts, and you might encounter some pages that are not implemented yet.
	<p>If you like this redesign, please share it with your friends and spread the word. And if you have any feedback or if you just want to say hi, feel free to email me.
